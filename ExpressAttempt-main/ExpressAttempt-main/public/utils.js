// fully visible
HTMLElement.prototype.isVisible = function() {
        var rect = this.getBoundingClientRect();
        return (
            rect.top >= 0 &&
            rect.left >= 0 &&
            rect.bottom <= (window.innerHeight || document.documentElement.clientHeight) && /*or $(window).height() */
            rect.right <= (window.innerWidth || document.documentElement.clientWidth) /*or $(window).width() */
        );
    }
    // visible as percentage
HTMLElement.prototype.isPartiallyVisible = function(percentageVisible) {
    if (!percentageVisible || arguments.length != 1) { throw new Error("isPartiallyVisible requires 1 argument. Got " + arguments.length) }
    let
        el = this,
        rect = el.getBoundingClientRect(),
        windowHeight = (window.innerHeight || document.documentElement.clientHeight);

    return !(
        Math.floor(100 - (((rect.top >= 0 ? 0 : rect.top) / + -(rect.height / 1)) * 100)) < percentageVisible ||
        Math.floor(100 - ((rect.bottom - windowHeight) / rect.height) * 100) < percentageVisible
    )
}
